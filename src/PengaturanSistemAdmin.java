    private void btnResetPassActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResetPassActionPerformed
     int row = tableUser.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Pilih user terlebih dahulu.");
        return;
    }

    String id = tableUser.getValueAt(row, 0).toString();
    String kolomId = tipeUser.equals("mahasiswa") ? "nim" : "nidn";
    String kolomPassword = tipeUser.equals("mahasiswa") ? "nama" : "password"; // gunakan ini

    String newPass = JOptionPane.showInputDialog(this, "Masukkan password baru:");
    if (newPass != null && !newPass.isEmpty()) {
        try {
            Connection conn = KoneksiDB.getConnection();
            String sql = "UPDATE " + tipeUser + " SET " + kolomPassword + " = ? WHERE " + kolomId + " = ?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, newPass);
            pst.setString(2, id);
            pst.executeUpdate();

            JOptionPane.showMessageDialog(this, "Password berhasil direset.");
            tampilkanDataUser(tipeUser); // ini akan refresh isi tabel

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal mereset password: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_btnResetPassActionPerformed

