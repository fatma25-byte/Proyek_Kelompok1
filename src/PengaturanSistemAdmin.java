    private void btnHapusUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusUserActionPerformed
     int row = tableUser.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Pilih user yang ingin dihapus.");
            return;
        }

        String id = tableUser.getValueAt(row, 0).toString();
        String kolom = tipeUser.equals("mahasiswa") ? "nim" : "nidn";

        int confirm = JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus user ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            try {
                Connection conn = KoneksiDB.getConnection();
                String sql = "DELETE FROM " + tipeUser + " WHERE " + kolom + " = ?";
                PreparedStatement pst = conn.prepareStatement(sql);
                pst.setString(1, id);
                pst.executeUpdate();

                JOptionPane.showMessageDialog(this, "User berhasil dihapus.");
                tampilkanDataUser(tipeUser);

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Gagal menghapus user: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnHapusUserActionPerformed

