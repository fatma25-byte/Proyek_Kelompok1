    private void tampilkanDataUser(String tipe) {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn(tipe.equals("mahasiswa") ? "NIM" : "NIDN");
        model.addColumn("Nama");

        try {
            Connection conn = KoneksiDB.getConnection();
            String sql = tipe.equals("mahasiswa") ?
                "SELECT nim AS id, nama FROM mahasiswa" :
                "SELECT nidn AS id, password FROM dosen";

            PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("id"),
                     
                tipe.equals("mahasiswa") ? rs.getString("nama") : rs.getString("password")
           
                });
            }

            tableUser.setModel(model);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data: " + e.getMessage());
        }
    }

   
    

