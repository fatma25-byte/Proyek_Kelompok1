    private void tampilRiwayat() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Tanggal");
        model.addColumn("File Skripsi");
        model.addColumn("Catatan");
        model.addColumn("Status");

        try {
            Connection conn = KoneksiDB.getConnection();
            String sql = "SELECT tanggal, file_skripsi, catatan, status_pengajuan FROM bimbingan WHERE nim = ? ORDER BY tanggal DESC";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, Session.getNIM());
            ResultSet rs = pst.executeQuery();

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("tanggal"),
                    rs.getString("file_skripsi"),
                    rs.getString("catatan"),
                    rs.getString("status_pengajuan")
                });
            }

            tableriwayat.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data: " + e.getMessage());
        }
        
    }
    private void tampilRiwayatJadwal() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("Tanggal");
    model.addColumn("Jam");
    model.addColumn("Keterangan");
    model.addColumn("Status");

    try {
        Connection conn = KoneksiDB.getConnection();
        String sql = "SELECT tanggal, jam, keterangan, status FROM jadwal_bimbingan WHERE nim = ? ORDER BY tanggal DESC";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, Session.getNIM());
        ResultSet rs = pst.executeQuery();

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("tanggal"),
                rs.getString("jam"),
                rs.getString("keterangan"),
                rs.getString("status")
            });
        }

        tabeljadwal.setModel(model);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal memuat riwayat jadwal: " + e.getMessage());
    }
}

