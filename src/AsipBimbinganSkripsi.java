    private void tampilkanArsipBimbingan() {
   DefaultTableModel model = new DefaultTableModel();
    model.addColumn("NIM");
    model.addColumn("Nama Mahasiswa");
    model.addColumn("Dosen Pembimbing");
    model.addColumn("File Skripsi");
    model.addColumn("Tanggal Disetujui");

    try {
        Connection conn = KoneksiDB.getConnection();
        String sql = "SELECT b.nim, m.nama, d.password AS dosen, b.file_skripsi, b.tanggal " +
                     "FROM bimbingan b " +
                     "JOIN mahasiswa m ON b.nim = m.nim " +
                     "JOIN dosen d ON m.nidn_pembimbing = d.nidn " +
                     "WHERE b.status_pengajuan = 'Disetujui' " +
                     "ORDER BY b.tanggal DESC";

        PreparedStatement pst = conn.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("nim"),
                rs.getString("nama"),
                rs.getString("dosen"),
                rs.getString("file_skripsi"),
                rs.getString("tanggal")
            });
        }

        tableArsip.setModel(model);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal memuat arsip: " + e.getMessage());
    }
}

