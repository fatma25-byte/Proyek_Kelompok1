    private void tampilkanDataMahasiswa() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("NIM");
    model.addColumn("Nama");
    model.addColumn("NIDN Pembimbing");

    try {
        Connection conn = KoneksiDB.getConnection();
        String sql = "SELECT * FROM mahasiswa";
        PreparedStatement pst = conn.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("nim"),
                rs.getString("nama"),
                rs.getString("nidn_pembimbing")
            });
        }

        tableMahasiswa.setModel(model);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal memuat mahasiswa: " + e.getMessage());
    }
}
    private void tampilkanDataDosen() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("NIDN");
    model.addColumn("password");

    try {
        Connection conn = KoneksiDB.getConnection();
        String sql = "SELECT * FROM dosen";
        PreparedStatement pst = conn.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("nidn"),
                rs.getString("password")
            });
        }

        tableDosen.setModel(model);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal memuat dosen: " + e.getMessage());
    }
}

