    private void btnEditMhsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditMhsActionPerformed
int row = tableMahasiswa.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Pilih baris mahasiswa yang ingin diedit.");
        return;
    }

    String nim = tableMahasiswa.getValueAt(row, 0).toString();
    String namaLama = tableMahasiswa.getValueAt(row, 1).toString();
    String nidnLama = tableMahasiswa.getValueAt(row, 2).toString();

    String namaBaru = JOptionPane.showInputDialog(this, "Edit Nama Mahasiswa:", namaLama);
    String nidnBaru = JOptionPane.showInputDialog(this, "Edit NIDN Pembimbing:", nidnLama);

    if (namaBaru != null && nidnBaru != null) {
        try {
            Connection conn = KoneksiDB.getConnection();
            String sql = "UPDATE mahasiswa SET nama = ?, nidn_pembimbing = ? WHERE nim = ?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, namaBaru);
            pst.setString(2, nidnBaru);
            pst.setString(3, nim);

            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data mahasiswa berhasil diperbarui.");
            tampilkanDataMahasiswa();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memperbarui: " + e.getMessage());
        }
    }  
    }//GEN-LAST:event_btnEditMhsActionPerformed

    private void btnEditDosenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditDosenActionPerformed
        int row = tableDosen.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Pilih baris dosen yang ingin diedit.");
        return;
    }

    String nidn = tableDosen.getValueAt(row, 0).toString();
    String namaLama = tableDosen.getValueAt(row, 1).toString();

    String namaBaru = JOptionPane.showInputDialog(this, "Edit Nama Dosen:", namaLama);

    if (namaBaru != null) {
        try {
            Connection conn = KoneksiDB.getConnection();
            String sql = "UPDATE dosen SET password = ? WHERE nidn = ?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, namaBaru);
            pst.setString(2, nidn);

            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data dosen berhasil diperbarui.");
            tampilkanDataDosen();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memperbarui: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_btnEditDosenActionPerformed

