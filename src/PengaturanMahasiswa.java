    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String passwordBaru = txtPassword.getText();

    if (passwordBaru.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Nama tidak boleh kosong.");
        return;
    }

    try {
        Connection conn = KoneksiDB.getConnection();
        String sql = "UPDATE pengaturan_akun SET password = ? WHERE nim = ?";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, passwordBaru);
        pst.setString(2, Session.getNIM());
        pst.executeUpdate();

        JOptionPane.showMessageDialog(this, "Data berhasil diperbarui.");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal menyimpan data: " + e.getMessage());
    }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnLogoutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogoutActionPerformed
        int pilihan = JOptionPane.showConfirmDialog(
        this,
        "Yakin ingin logout?",
        "Konfirmasi Logout",
        JOptionPane.YES_NO_OPTION
    );

    if (pilihan == JOptionPane.YES_OPTION) {
        this.dispose();
        new SISKRIP().setVisible(true); // ganti sesuai nama form login kamu
    }
    }//GEN-LAST:event_btnLogoutActionPerformed

