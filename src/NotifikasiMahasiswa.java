    private void tampilkanNotifikasi() {
    StringBuilder notifBimbingan = new StringBuilder();
    StringBuilder notifJadwal = new StringBuilder();

    try {
        Connection conn = KoneksiDB.getConnection();

        // --- Ambil Riwayat Bimbingan ---
        String sql1 = "SELECT tanggal, status_pengajuan, catatan FROM bimbingan WHERE nim = ? ORDER BY tanggal DESC LIMIT 5";
        PreparedStatement pst1 = conn.prepareStatement(sql1);
        pst1.setString(1, Session.getNIM());
        ResultSet rs1 = pst1.executeQuery();

        while (rs1.next()) {
            String tanggal = rs1.getString("tanggal");
            String status = rs1.getString("status_pengajuan");
            String catatan = rs1.getString("catatan");
            notifBimbingan.append("ðŸ“„ " + tanggal + " â€” " + status + ": \"" + catatan + "\"\n");
        }

        // --- Ambil Jadwal Bimbingan ---
        String sql2 = "SELECT tanggal, jam, status FROM jadwal_bimbingan WHERE nim = ? ORDER BY tanggal DESC LIMIT 5";
        PreparedStatement pst2 = conn.prepareStatement(sql2);
        pst2.setString(1, Session.getNIM());
        ResultSet rs2 = pst2.executeQuery();

        while (rs2.next()) {
            String tanggal = rs2.getString("tanggal");
            String jam = rs2.getString("jam");
            String status = rs2.getString("status");
            notifJadwal.append("ðŸ“… " + tanggal + " â€” " + jam + " â€” " + status + "\n");
        }

        // Tampilkan ke GUI
        lblNim.setText("NIM: " + Session.getNIM());
        areaBimbingan.setText(notifBimbingan.toString());
        areaJadwal.setText(notifJadwal.toString());

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal memuat notifikasi: " + e.getMessage());
    }
}

