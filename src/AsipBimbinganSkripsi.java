    private void btnUnduhActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUnduhActionPerformed
        int row = tableArsip.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Pilih skripsi yang ingin diunduh.");
        return;
    }

    String namaFile = tableArsip.getValueAt(row, 3).toString(); // kolom file_skripsi
    File file = new File("skripsi/" + namaFile); // path folder tempat kamu simpan file

    if (file.exists()) {
        try {
            Desktop.getDesktop().open(file); // buka file langsung
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Gagal membuka file: " + e.getMessage());
        }
    } else {
        JOptionPane.showMessageDialog(this, "File tidak ditemukan di folder skripsi/");
    }
    }//GEN-LAST:event_btnUnduhActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
       int row = tableArsip.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Pilih arsip yang ingin dihapus.");
        return;
    }

    String nim = tableArsip.getValueAt(row, 0).toString();
    String file = tableArsip.getValueAt(row, 3).toString();

    int konfirmasi = JOptionPane.showConfirmDialog(this, "Yakin hapus arsip ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
    if (konfirmasi == JOptionPane.YES_OPTION) {
        try {
            Connection conn = KoneksiDB.getConnection();
            String sql = "DELETE FROM bimbingan WHERE nim = ? AND file_skripsi = ?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, nim);
            pst.setString(2, file);

            int hasil = pst.executeUpdate();
            if (hasil > 0) {
                JOptionPane.showMessageDialog(this, "Arsip berhasil dihapus.");
                tampilkanArsipBimbingan(); // refresh tabel
            } else {
                JOptionPane.showMessageDialog(this, "Gagal menghapus arsip.");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_btnHapusActionPerformed

